#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - function that add two interger
 * @a: interger value of the parameter
 * @b: interger value of the parameter
 * Return: Always 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that add two interger
 * @a: interger value of the parameter
 * @b: interger value of the parameter
 * Return: Always 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that add two interger
 * @a: interger value of the parameter
 * @b: interger value of the parameter
 * Return: Always 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that add two interger
 * @a: interger value of the parameter
 * @b: interger value of the parameter
 * Return: Always 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that add two interger
 * @a: interger value of the parameter
 * @b: interger value of the parameter
 * Return: Always 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
